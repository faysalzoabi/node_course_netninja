const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const blogSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    snippet: {
        type: String,
        required: true
    },
    body: {
        type: String,
        required: true
    }
}, {timestamps: true}); // automatically generate timestamp property on our blog schema

const Blog = mongoose.model('Blog', blogSchema);

module.exports = Blog;